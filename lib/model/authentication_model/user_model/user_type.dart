enum Type { INSTRUCTOR, STUDENT }

final typeValues =
    EnumValues({"INSTRUCTOR": Type.INSTRUCTOR, "STUDENT": Type.STUDENT});

class EnumValues<T> {
  Map<String, T> map;
  Map<T, String> reverseMap;

  EnumValues(this.map);

  Map<T, String> get reverse {
    if (reverseMap == null) {
      reverseMap = map.map((k, v) => new MapEntry(v, k));
    }
    return reverseMap;
  }
}
